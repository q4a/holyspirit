Crédits
Une vieille loi obligeait depuis toujours le Bien et le Mal à respecter au moins une règle dans leur éternel conflit. Ils ne pouvaient intervenir directement dans les affaires des entités vivantes, n'agissant que par la suggestion et bataillant subtilement au sein de chaque âme...\Egoïsme, violence, pitié, amour s'affrontaient ainsi sous forme de pensées, de rêves, avec des destins variés.\\Pourtant un jour le Mal, qui ne supportait plus l'équilibre dont se contentait le Bien, décida de violer la règle respectée jusqu'alors et franchit le pas en s'incarnant directement dans les êtres vivants les plus vulnérables à ses pulsions, et, curieusement, dans certains objets.\Désormais le Mal pouvait régner exclusivement sur chaque être dont il prenait possession et sur certains objets anodins devenus démoniaques.\\Ayant pressenti cette trahison, le Bien ne fut pas surpris. Renonçant à violer la loi comme le Mal l'avait fait et se refusant à sacrifier des âmes bonnes et généreuses, il utilisa quelques âmes damnées et tourmentées qui souhaitaient profondément se racheter auprès de lui...\S'estimant déjà perdues, elles seules auraient l'inconscience de s'attaquer a l'immonde légion qui déferlait sur le Monde.
