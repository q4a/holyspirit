
main 
	if talk
	then
		variable * 2 * 0
		
		if variable * 0 * 1
		then
			variable * 0 * 0
		else
			variable * 0 * 1
		end
		
		stop_talk
	end
	
	if variable * 2 * 0
	then
		if variable * 1 * 0
		then
			if variable * 0 * 1
			then
				setState * 3 * 0
				setCollision * 0
			else
				setState * 0 * 0
				setCollision * 1
			end
		else
			setState * 0 * 0
			setCollision * 1
		end
	end
	
	variable * 2 * 1
end 
