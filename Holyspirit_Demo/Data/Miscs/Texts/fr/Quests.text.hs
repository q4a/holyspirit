Les choses qui rodaient dans les catacombes
Je dois aller dans les catacombes voir ce qu'il s'y passe.






La charette abandonnée
Je dois trouver et rapporter à Romulus le marchand le sachet de tabac abandonné dans sa charrette .
Je dois rapporter le tabac à Romulus le marchand.
Romulus a récupéré son tabac.

Un passe temps pour Rognorak		
Rognorak le forgeron s'ennuie tant qu'il m'a proposé de me fabriquer une arme gratuitement. Je dois lui apporter un schéma et les ingrédients nécessaires.
Rognorak a ce qu'il lui faut pour réaliser une arme.

La bénédiction du bon abbé
L'abbé peut bénir un de mes objets si je lui fournis de l'eau bénite et une litanie.
L'abbé a béni un de mes objets.

Le muret diabolique
Je dois éliminer les créatures qui ont infesté le muret du paysan Lothaire.
Toutes les créatures ont été éliminées.

L'aubergiste assiégé
Je dois libérer le passage devant l'auberge.
L'aubergiste a pu s'échapper.

Une maison dans la forêt
Je dois secourir une jeune femme qui est terrée dans sa maison au sud-est de l'abbaye, dans la forêt.
La jeune femme n'était hélas plus elle-même. J'ai dû mettre fin à sa nouvelle horrible existence.

Une créature dans la nuit
