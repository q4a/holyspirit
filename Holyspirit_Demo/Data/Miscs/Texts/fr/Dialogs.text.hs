////ABBE///
Salut à toi mon fils ! C’est toi ? Oui ça ne peut être que toi ! Grand, fort… Le Sauveur ! Venu de nulle part ! Dieu m’a parlé, et je savais qu’il ne nous abandonnerait pas ! Vas, extermine cette vermine démoniaque qui rode partout alentours !
Qui es tu femme ? De quelle contrée viens-tu donc avec ce maquillage suggestif, ces habits étranges ? Es-tu un démon qui a réussi à s’immiscer dans la maison de Dieu ? Fais bien attention à tes actes, même en cette période troublée on peut trouver le temps de brûler les sorcières…
Je reconnais en toi un serviteur de Dieu mon frère. Mais la fièvre que je lis dans ton regard m’inquiète quelque peu… Est-ce que tu vas bien ?
Salut à toi mon fils. Tu dois venir d’un pays bien lointain pour errer dans un tel accoutrement.\Peut-être que tu sauras comment nous débarrasser de la vermine qui s’abat sur notre pauvre contrée ?
Où suis-je ?
Tu es dans l’abbaye de Saint Clodomir. Par les temps qui courent c’est l’un des rares lieux de la contrée où on trouve un peu de calme. Et encore… Même ici il se trame des diableries !
Des diableries ?
Oui, parfaitement, des diableries. Mes frères moines on un comportement étrange, parfois malsain et certains ont mystérieusement disparu… J’entends des bruits dans les catacombes sous l’abbaye la nuit. J’ai d’abord cru au bruit du vent, puis j’ai dû m’y résoudre : ce sont des incantations, des gémissements malsains parfois… J’ai peur, je dois l’avouer, et la seule solution que j’ai trouvée est de fermer la porte qui y mène. Mais il faudra bien que quelqu’un aille voir ce qui se trame là-bas… Je suis inquiet pour mes moines !
Voulez-vous que j’aille voir ?
Oh ! Je n’osais pas demander… J’ai quelques scrupules à t’envoyer là-bas en solitaire, mais nous ne sommes pas des combattants vois-tu… Mais si tu as besoin d’aide je ferai ce que je peux pour t’aider ! Tiens, prends la clef des catacombes et bonne chance, Dieu t’assistera !
C’est bien ce qu’il me semblait ! Il se tramait des choses monstrueuses derrière cette porte… Décidément nous sommes cernés par le mal ! Il est dehors, il est sous nos pieds ! Est-ce que le ciel lui-même est vraiment bienveillant pour nous, pauvres misérables humains ? 
Enfin, tu as trouvé la source du mal ! Me voilà rassuré, mais pas tant que ça… Le monde entier semble envahi par cette fange répugnante de morts-vivants et d’objets mystérieusement animés… je crois que la fin de cette période chaotique est encore loin hélas !
Je cherche des armes.
Tu peux demander à Rognorak, notre forgeron qui s’est réfugié avec nous dans ce lieu saint. C’est un grand gaillard sympathique, il continue à forger des armes même si ça ne sert plus à rien, il dit que ça l’occupe en attendant la fin du monde.
Avez-vous des choses à vendre ?
Pourriez-vous me bénir un objet ?
Au revoir !





